# Tuple Basics
number = (3, 4, 5, 6, 7, 8, 9)
print(number)
print(type(number))

# Indexing
print(number[0])
print(number[1])
print(number[2])

# Slicing
num = number[2:4]
print(num)
print(type(num))

# Tuple Methods (index,count)
numbers = (3, 4, 5, 6, 7, 4, 8, 9)
print(numbers)
print(numbers.index(4))
print(numbers.count(4))
