a = 9
b = 4

# Arithmetic Operators: Perform mathematical calculations.(+,-,*,/,%(Modulus-remainder),**(Exponentiation))
print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a % b)
print(a**b)

# Comparison (Relational) Operators: Compare two values and return a Boolean (True or False).(==,!=,<,>,<=,>=)
print(a == b)
print(a != b)
print(a < b)
print(a > b)
print(a <= b)
print(a >=b)

# Assignment Operators: Assign values to variables, often combined with an operation (Augmented Assignment).(=, +=, -=, *=, /=, //=, %=, **=).
num = 10

num += 10
num -= 10
num *= 10
num /= 10
num *= 10
num //= 10
num **= 10
print(num)

# Logical Operators: Combine conditional statements.(not,and,or)
print(not False)
print(not True)

value1 = False
value2 = True

print("And Operator answer is :", (value1 and value2))
print("OR Operator answer is :", (value1 or value2))
