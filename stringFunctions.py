# String with End With
str1 = "I am a Developer."
print(str1.endswith("er."))

# String with Capitalize
str2 = "i am a developer."
print(str2.capitalize())

# String with Replace
str3 = "I am a JavaScript Developer."
print(str3.replace("JavaScript", "Python"))

# String with Find
str4 = "I am a Python Developer."
print(str4.find("Python"))

# String with Count
str5 = "I am a Python and a JavaScript Developer."
print(str5.count("a"))
