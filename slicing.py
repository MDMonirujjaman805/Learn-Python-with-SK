# Slicing
name3 = "monir bin khaled"
slice_str1 = name3[0:6]  # with starting index and ending index.
slice_str2 = name3[6 : len(name3)]  # with length of string.
slice_str3 = name3[6:]  # only starting index.
slice_str4 = name3[:5]  # only ending index.
print(slice_str1)
print(slice_str2)
print(slice_str3)
print(slice_str4)
