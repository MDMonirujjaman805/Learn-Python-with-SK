# escape sequence character (\n,\t,\",\',\r,\b)

str1 = "This is Python Language, \nI will learn python for AI."
str2 = "This is Python Language, \tI will learn python for AI."
str3 = 'This is Python Language, " I will learn python for AI.'
str4 = "This is Python Language, 'I will learn python for AI."
str5 = "This is Python Language, \rI will learn python for AI."
str6 = "This is Python Language, \bI will learn python for AI."
print(str1)
print(str2)
print(str3)
print(str4)
print(str5)
print(str6)
